int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i <= nb / 2)
	{
		if ((nb % i) == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int i;
	int prime;

	i = nb;
	prime = ft_is_prime(nb);
	while (prime != 1)
	{
		i++;
		prime = ft_is_prime(i);
	}
	return (i);
}
