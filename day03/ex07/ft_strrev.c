char	*ft_strrev(char *str)
{
	char	temp;
	int		i;
	int		b;

	i = 0;
	while (str[i] != '\0')
		i++;
	b = 0;
	i = i - 1;
	while (b < i)
	{
		temp = str[i];
		str[i] = str[b];
		str[b] = temp;
		b++;
		i--;
	}
	return (str);
}
